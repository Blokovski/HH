package com.nm.tits;

public class Profile
{
	private String displayName;
	private String numberOfPictures;
	private String averageRating;
	private String avatar;
	private String email;
	private String topListPosition;
	private String country;
	private String city;
	
	public String getDisplayName()
	{
		return displayName;
	}
	public void setDisplayName(String displayName)
	{
		this.displayName = displayName;
	}
	public String getNumberOfPictures()
	{
		return numberOfPictures;
	}
	public void setNumberOfPictures(String numberOfPictures)
	{
		this.numberOfPictures = numberOfPictures;
	}
	public String getAverageRating()
	{
		return averageRating;
	}
	public void setAverageRating(String averageRating)
	{
		this.averageRating = averageRating;
	}
	public String getAvatar()
	{
		return avatar;
	}
	public void setAvatar(String avatar)
	{
		this.avatar = avatar;
	}
	public String getEmail()
	{
		return email;
	}
	public void setEmail(String email)
	{
		this.email = email;
	}
	public String getTopListPosition()
	{
		return topListPosition;
	}
	public void setTopListPosition(String topListPosition)
	{
		this.topListPosition = topListPosition;
	}
	public String getCountry()
	{
		return country;
	}
	public void setCountry(String country)
	{
		this.country = country;
	}
	public String getCity()
	{
		return city;
	}
	public void setCity(String city)
	{
		this.city = city;
	}
}
